#!/usr/bin/env node
/**
 * Generates register-*.ts chunk entry files in src/chunks/ from component-registry.json.
 * Also generates the vite.config.ts entry map snippet and the env var exclusion list.
 *
 * Usage: node scripts/generate-chunk-entries.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const registry = JSON.parse(readFileSync("/Users/jonudell/wrapped/trace-tools/component-registry.json", "utf-8"));
const manifest = JSON.parse(readFileSync(join(root, "scripts", "chunk-manifest.json"), "utf-8"));

// Invert chunkEnvVars: envVar → chunkId
const envVarToChunkId = {};
for (const [chunkId, envVar] of Object.entries(manifest.chunkEnvVars)) {
  envVarToChunkId[envVar] = chunkId;
}

const chunksDir = join(root, "src", "chunks");
mkdirSync(chunksDir, { recursive: true });

const entries = {};
const envVarExclusions = {};

for (const group of registry) {
  const chunkId = envVarToChunkId[group.envVar];
  if (!chunkId) {
    console.warn(`WARNING: No chunkId found for envVar ${group.envVar}, skipping`);
    continue;
  }

  const filename = `register-${chunkId}.ts`;

  // Collect unique import paths and their renderers
  const importsByPath = {};
  for (const reg of group.registrations) {
    const importPath = `../components/${reg.importPath.replace(/^\.\//, "")}`;
    if (!importsByPath[importPath]) {
      importsByPath[importPath] = [];
    }
    importsByPath[importPath].push(reg.renderer);
  }

  // Generate import statements
  const imports = [];
  const rendererNames = [];
  for (const [path, renderers] of Object.entries(importsByPath)) {
    imports.push(`import { ${renderers.join(", ")} } from "${path}";`);
    rendererNames.push(...renderers);
  }

  const code = `// Component chunk: ${chunkId}
// Auto-generated by scripts/generate-chunk-entries.mjs
// Self-registers via the global Xmlui extension manager.
${imports.join("\n")}

(window as any).Xmlui?.registerExtension({
  namespace: "#xmlui-core-ns",
  components: [${rendererNames.join(", ")}],
});
`;

  writeFileSync(join(chunksDir, filename), code);
  entries[`chunk-${chunkId}`] = `path.resolve("src", "chunks", "${filename}")`;
  envVarExclusions[group.envVar] = '"false"';
}

console.log(`Generated ${Object.keys(entries).length} chunk entry files in src/chunks/`);
console.log();
console.log("=== Vite entry map (for vite.config.ts) ===");
console.log("{");
console.log(`  "xmlui-core": path.resolve("src", "index-standalone.ts"),`);
for (const [name, path] of Object.entries(entries)) {
  console.log(`  "${name}": ${path},`);
}
console.log("}");
console.log();
console.log("=== Env var exclusions (for define block) ===");
for (const [envVar, val] of Object.entries(envVarExclusions)) {
  console.log(`  ${envVar}: ${val},`);
}
