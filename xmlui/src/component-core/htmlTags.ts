export const htmlTagDefinitions: HtmlTagDefinition[] = htmlTagNames.map((name) => ({
  name,
  metadataName: `Html${htmlMetadataSuffix(name)}`,
  tagName: name,
  voidElement: voidHtmlTags.has(name),
}));

export const htmlTagComponentNames = htmlTagDefinitions.map((definition) => definition.name);

function htmlMetadataSuffix(name: string): string {
  return name
    .split("-")
    .map((part) => part.length === 1 ? part.toUpperCase() : `${part[0]?.toUpperCase() ?? ""}${part.slice(1)}`)
    .join("");
}
